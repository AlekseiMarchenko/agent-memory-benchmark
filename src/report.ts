import { BenchmarkResult } from "./types.js";
import { getGrade, getGradeColor } from "./scoring.js";
import * as fs from "fs";
import * as path from "path";

export function generateMarkdown(result: BenchmarkResult): string {
  const grade = getGrade(result.overallScore);
  let md = `# Agent Memory Benchmark Results\n\n`;
  md += `**Provider:** ${result.provider}\n`;
  md += `**Date:** ${result.timestamp}\n`;
  md += `**AMB Version:** ${result.version}\n`;
  md += `**Overall Score:** ${result.overallScore}/100 (${grade})\n\n`;

  md += `## Category Scores\n\n`;
  md += `| Category | Score | Passed | Avg Latency |\n`;
  md += `|---|---|---|---|\n`;

  for (const cat of result.categories) {
    if (cat.skipped) {
      md += `| ${cat.name} | ⏭️ Skipped | — | — |\n`;
    } else {
      const catGrade = getGrade(cat.score);
      md += `| ${cat.name} | ${cat.score.toFixed(0)}% (${catGrade}) | ${cat.passed}/${cat.total} | ${cat.avgLatencyMs}ms |\n`;
    }
  }

  md += `\n## Efficiency Metrics\n\n`;
  md += `| Metric | Value |\n`;
  md += `|---|---|\n`;
  md += `| Total API Calls | ${result.meta.totalApiCalls} |\n`;
  md += `| Total Latency | ${(result.meta.totalLatencyMs / 1000).toFixed(1)}s |\n`;
  md += `| Est. Token Usage | ${result.meta.totalTokens.toLocaleString()} |\n`;

  // Failed tests
  const failures = result.categories.flatMap((c) => c.details.filter((d) => !d.passed));
  if (failures.length > 0) {
    md += `\n## Failed Tests (${failures.length})\n\n`;
    for (const f of failures.slice(0, 20)) {
      md += `### ${f.queryId}: "${f.query}"\n`;
      md += `- **Reason:** ${f.reason}\n`;
      if (f.topResults.length > 0) {
        md += `- **Top result:** "${f.topResults[0].content.slice(0, 100)}..."\n`;
      }
      md += `\n`;
    }
    if (failures.length > 20) {
      md += `_...and ${failures.length - 20} more failures_\n`;
    }
  }

  md += `\n---\n_Generated by [Agent Memory Benchmark](https://github.com/AlekseiMarchenko/agent-memory-benchmark) v${result.version}_\n`;

  return md;
}

export function generateBadgeSvg(score: number, provider: string): string {
  const grade = getGrade(score);
  const color = getGradeColor(score);
  const label = "AMB Score";
  const value = `${score} (${grade})`;

  return `<svg xmlns="http://www.w3.org/2000/svg" width="180" height="20">
  <linearGradient id="b" x2="0" y2="100%">
    <stop offset="0" stop-color="#bbb" stop-opacity=".1"/>
    <stop offset="1" stop-opacity=".1"/>
  </linearGradient>
  <clipPath id="a">
    <rect width="180" height="20" rx="3" fill="#fff"/>
  </clipPath>
  <g clip-path="url(#a)">
    <rect width="90" height="20" fill="#555"/>
    <rect x="90" width="90" height="20" fill="${color}"/>
    <rect width="180" height="20" fill="url(#b)"/>
  </g>
  <g fill="#fff" text-anchor="middle" font-family="DejaVu Sans,Verdana,Geneva,sans-serif" font-size="11">
    <text x="45" y="15" fill="#010101" fill-opacity=".3">${label}</text>
    <text x="45" y="14">${label}</text>
    <text x="135" y="15" fill="#010101" fill-opacity=".3">${value}</text>
    <text x="135" y="14">${value}</text>
  </g>
</svg>`;
}

export function writeResults(result: BenchmarkResult, outputDir: string): void {
  fs.mkdirSync(outputDir, { recursive: true });

  // JSON
  fs.writeFileSync(
    path.join(outputDir, "results.json"),
    JSON.stringify(result, null, 2)
  );

  // Markdown
  fs.writeFileSync(
    path.join(outputDir, "report.md"),
    generateMarkdown(result)
  );

  // Badge
  fs.writeFileSync(
    path.join(outputDir, "badge.svg"),
    generateBadgeSvg(result.overallScore, result.provider)
  );

  console.log(`📄 Results written to ${outputDir}/`);
  console.log(`   - results.json`);
  console.log(`   - report.md`);
  console.log(`   - badge.svg`);
}
